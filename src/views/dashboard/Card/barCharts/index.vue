      yAxis: {
        //隐藏y轴
        show: false,
      },
      //系列
      series: [
        {
          //图标形式-柱状图
          type: "bar",
          data: [
            15,
            12,
            14,
            11,
            {
              value: 10,
              itemStyle: {
                color: "#ee6666",
              },
            },
            19,
            9,
